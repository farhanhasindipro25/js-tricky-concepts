// JavaScript: Dynamically Typed Programming Language.

// Primitive Data Types
// Integers (Number)
const a = 5;
// Floats/Doubles (Number)
const b = 2.2;
// Strings
const c = "ABCDE";
// boolean
const d = true;

console.log(typeof a);
console.log(typeof b);
console.log(typeof c);
console.log(typeof d);

// Non-primitive Data Types
// Arrays
const arr = [1, 2, 3, 4, 5];
// Objects
const obj = { A: 1, B: 2 };


